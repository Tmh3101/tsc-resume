"use client";

import React from "react";
import Image from "next/image";
import { motion, AnimatePresence } from "framer-motion";
import { X, Github, Loader2 } from "lucide-react";
import { createClient } from "@/utils/supabase/client";
import Link from "next/link";

// =====================================================
// Login Modal Component
// Modal đăng nhập với Google và GitHub (OAuth Popup)
// =====================================================

interface LoginModalProps {
    isOpen: boolean;
    onClose: () => void;
}

export function LoginModal({ isOpen, onClose }: LoginModalProps) {
    const [isLoading, setIsLoading] = React.useState<'google' | 'github' | null>(null);
    const [error, setError] = React.useState<string | null>(null);

    // Close modal khi nhấn Escape
    React.useEffect(() => {
        const handleEscape = (e: KeyboardEvent) => {
            if (e.key === "Escape" && !isLoading) onClose();
        };

        if (isOpen) {
            document.addEventListener("keydown", handleEscape);
            document.body.style.overflow = "hidden";
        }

        return () => {
            document.removeEventListener("keydown", handleEscape);
            document.body.style.overflow = "unset";
        };
    }, [isOpen, onClose, isLoading]);

    // Reset states when modal closes
    React.useEffect(() => {
        if (!isOpen) {
            setIsLoading(null);
            setError(null);
        }
    }, [isOpen]);

    const handleOAuthLogin = async (provider: 'google' | 'github') => {
        setIsLoading(provider);
        setError(null);

        try {
            const supabase = createClient();
            
            const { data, error } = await supabase.auth.signInWithOAuth({
                provider,
                options: {
                    redirectTo: `${window.location.origin}/auth/callback`,
                    queryParams: provider === 'google' ? {
                        access_type: 'offline',
                        prompt: 'consent',
                    } : undefined,
                },
            });

            if (error) {
                throw error;
            }

            // OAuth sẽ redirect, không cần làm gì thêm
        } catch (err) {
            console.error(`${provider} login error:`, err);
            setError(err instanceof Error ? err.message : 'Đã xảy ra lỗi khi đăng nhập');
            setIsLoading(null);
        }
    };

    const handleGoogleLogin = () => handleOAuthLogin('google');
    const handleGithubLogin = () => handleOAuthLogin('github');

    return (
        <AnimatePresence>
            {isOpen && (
                <>
                    {/* Backdrop */}
                    <motion.div
                        initial={{ opacity: 0 }}
                        animate={{ opacity: 1 }}
                        exit={{ opacity: 0 }}
                        onClick={onClose}
                        className="fixed inset-0 bg-black/60 backdrop-blur-sm z-[100]"
                    />

                    {/* Modal */}
                    <motion.div
                        initial={{ opacity: 0, scale: 0.95, y: 20 }}
                        animate={{ opacity: 1, scale: 1, y: 0 }}
                        exit={{ opacity: 0, scale: 0.95, y: 20 }}
                        transition={{ type: "spring", damping: 25, stiffness: 300 }}
                        className="fixed inset-0 flex items-center justify-center z-[101] p-4"
                    >
                        <div 
                            className="bg-white rounded-2xl shadow-2xl w-full max-w-md overflow-hidden"
                            onClick={(e) => e.stopPropagation()}
                        >
                            {/* Header with gradient */}
                            <div className="relative bg-gradient-to-br from-deep-blue via-deep-blue-light to-orange/80 px-6 py-8 text-center">
                                {/* Close button */}
                                <button
                                    onClick={onClose}
                                    className="absolute top-4 right-4 p-2 rounded-full bg-white/10 hover:bg-white/20 transition-colors"
                                    aria-label="Đóng"
                                >
                                    <X className="w-5 h-5 text-white" />
                                </button>

                                {/* Logo TSC */}
                                <div className="flex items-center justify-center gap-2 mb-4">
                                    <Image
                                        src="/logo-tsc.png"
                                        alt="TSC Logo"
                                        width={48}
                                        height={48}
                                        className="rounded-xl"
                                    />
                                    <span className="text-3xl font-bold text-white">
                                        <span className="text-orange">T</span>SC
                                    </span>
                                </div>

                                <h2 className="text-2xl font-bold text-white mb-2">
                                    Đăng nhập
                                </h2>
                                <p className="text-white/80 text-sm">
                                    Đăng nhập để sử dụng đầy đủ tính năng CV Tools
                                </p>
                            </div>

                            {/* Body */}
                            <div className="p-6 space-y-4">
                                {/* Error Message */}
                                {error && (
                                    <div className="p-3 rounded-lg bg-red-50 border border-red-200 text-red-600 text-sm text-center">
                                        {error}
                                    </div>
                                )}

                                {/* Google Login Button */}
                                <button
                                    onClick={handleGoogleLogin}
                                    disabled={isLoading !== null}
                                    className="
                                        w-full flex items-center justify-center gap-3
                                        px-4 py-3 rounded-xl
                                        bg-white border-2 border-gray-200
                                        text-gray-700 font-medium
                                        hover:bg-gray-50 hover:border-gray-300
                                        disabled:opacity-60 disabled:cursor-not-allowed
                                        transition-all duration-200
                                    "
                                >
                                    {isLoading === 'google' ? (
                                        <Loader2 className="w-5 h-5 animate-spin" />
                                    ) : (
                                        <svg className="w-5 h-5" viewBox="0 0 24 24">
                                            <path
                                                fill="#4285F4"
                                                d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
                                            />
                                            <path
                                                fill="#34A853"
                                                d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
                                            />
                                            <path
                                                fill="#FBBC05"
                                                d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
                                            />
                                            <path
                                                fill="#EA4335"
                                                d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
                                            />
                                        </svg>
                                    )}
                                    <span>{isLoading === 'google' ? 'Đang đăng nhập...' : 'Tiếp tục với Google'}</span>
                                </button>

                                {/* GitHub Login Button */}
                                <button
                                    onClick={handleGithubLogin}
                                    disabled={isLoading !== null}
                                    className="
                                        w-full flex items-center justify-center gap-3
                                        px-4 py-3 rounded-xl
                                        bg-gray-900 border-2 border-gray-900
                                        text-white font-medium
                                        hover:bg-gray-800 hover:border-gray-800
                                        disabled:opacity-60 disabled:cursor-not-allowed
                                        transition-all duration-200
                                    "
                                >
                                    {isLoading === 'github' ? (
                                        <Loader2 className="w-5 h-5 animate-spin" />
                                    ) : (
                                        <Github className="w-5 h-5" />
                                    )}
                                    <span>{isLoading === 'github' ? 'Đang đăng nhập...' : 'Tiếp tục với GitHub'}</span>
                                </button>
                            </div>

                            {/* Footer */}
                            <div className="px-6 py-4 bg-gray-50 border-t border-gray-100">
                                    <p className="text-xs text-gray-500 text-center">
                                        Bằng việc đăng nhập, bạn đồng ý với{" "}
                                        <Link
                                            href="/terms-of-service"
                                            target="_blank"
                                            className="text-orange hover:underline"
                                        >
                                            Điều khoản sử dụng
                                        </Link>{" "}
                                        và{" "}
                                        <Link
                                            href="/privacy-policy"
                                            target="_blank"
                                            className="text-orange hover:underline"
                                        >
                                            Chính sách bảo mật
                                        </Link>
                                    </p>
                            </div>
                        </div>
                    </motion.div>
                </>
            )}
        </AnimatePresence>
    );
}
